package javatraining.flowcontrols;

public class UsingSwitchApplication {
    public static void main(String[] args) {
        int num = 4;
        switch(num) {
            case 1 :
                System.out.println(num);
                break;
            case 2:
                System.out.println(num);
                break;
            case 3:
                System.out.println(num);
                break;
            case 4:
                System.out.println(num);
                break;
            default:
                System.out.println(34);

        }
    }

}
