package javatraining.variablesanddatatypes;

public class SwitchUsingCharacter {
    public static void main(String[] args) {
        String c = "Switch";
        switch (c) {
            case "abc":
                System.out.println("df");
                break;
            case "def":
                System.out.println("def");
                break;
            case "ghi":
                System.out.println("ghi");
                break;
            case "Switch":
                System.out.println("Switch is a statement");
                break;

        }
    }
}
